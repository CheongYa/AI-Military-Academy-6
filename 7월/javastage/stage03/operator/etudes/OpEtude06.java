package stage03.operator.etudes;

public class OpEtude06 {
	public static void main(String[] args) {
		int a = 10;
		
		a++; //a = a + 1;
		System.out.println(a);
		++a; //a = a + 1;
		System.out.println(a);
		a--; //a = a - 1;
		System.out.println(a);
		--a; //a = a - 1;
		System.out.println(a);
		
		System.out.println("au revoir!");
	}
}
