package stage03.operator.etudes;

public class OpEtude08 {
	public static void main(String[] args) {
		int a = 10;
		
		a += 2;  //a = a + 2;
		System.out.println(a);
		a -= 2;
		System.out.println(a);
		a *= 2;
		System.out.println(a);
		a /= 2;
		System.out.println(a);
		a %= 2;
		System.out.println(a);
		
		System.out.println("au revoir!");
	}
}
